<h3>Что такое Linux?</h3>
<p>
Linux - в широком смысле, это свободно распространяемая операционная система с открытым исходным кодом. Говоря более строго, Linux - это название ядра, главной составляющей множества UNIX-совместимых операционных систем, таких как, Debian, Red Hat, Slackware и т.д. и т.п.
</p>
<p>
Linux является одним из главных достижений OpenSource движения. Системы Linux, имея современные технологические характеристики, отличаются от проприетарных операционных систем в первую очередь идеологически. Ядро, разработанное первоначально Линусом Торвальдсом (Linux Torvalds) в Университете Хельсинки (Финляндия), получило признание и поддержку сообществом программистов по всему миру и обросло множеством программ: от утилит командной строки до офисных пакетов и игр. Практически всё программное обеспечение для Linux распространяется бесплатно и в исходных кодах. Например, значительная часть программного обеспечения создаётся в рамках проекта FSF GNU, продукция которого лицензируется, как и само ядро Linux, по лицензии GNU General Public License, предполагающей, кроме прочего, наследование статуса и идеологии "free software" для лицензирумых программ.
</p>

